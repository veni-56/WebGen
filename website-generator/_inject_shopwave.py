@app.route('/coupon/validate', methods=['POST'])
@login_required
def validate_coupon():
    code=request.json.get('code','').strip().upper()
    total=float(request.json.get('total',0))
    conn=get_db()
    c=conn.execute("SELECT * FROM coupons WHERE code=? AND is_active=1",(code,)).fetchone()
    conn.close()
    if not c: return jsonify({'valid':False,'message':'Invalid coupon code.'})
    if c['expires_at'] and datetime.strptime(c['expires_at'],'%Y-%m-%d %H:%M:%S') < datetime.now():
        return jsonify({'valid':False,'message':'Coupon has expired.'})
    if c['max_uses'] > 0 and c['used_count'] >= c['max_uses']:
        return jsonify({'valid':False,'message':'Coupon usage limit reached.'})
    if total < c['min_order']:
        return jsonify({'valid':False,'message':f'Minimum order ₹{c["min_order"]:.0f} required.'})
    if c['discount_type']=='percent':
        discount=round(total*c['discount_value']/100,2)
        msg=f'{c["discount_value"]:.0f}% off applied!'
    else:
        discount=min(c['discount_value'],total)
        msg=f'₹{discount:.0f} off applied!'
    return jsonify({'valid':True,'discount':discount,'message':msg,'code':code})

