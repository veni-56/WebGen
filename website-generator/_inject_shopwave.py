@app.route('/checkout', methods=['GET','POST'])
@login_required
def checkout():
    conn=get_db()
    items=conn.execute("""SELECT ci.id,ci.quantity,p.id as pid,p.name,p.price,p.stock,p.seller_id
           FROM cart_items ci JOIN products p ON ci.product_id=p.id WHERE ci.user_id=?""",
        (session['user_id'],)).fetchall()
    if not items:
        flash('Your cart is empty.','warning'); conn.close(); return redirect(url_for('cart'))
    subtotal=sum(i['price']*i['quantity'] for i in items)
    if request.method=='POST':
        address=request.form.get('address','').strip()
        coupon_code=request.form.get('coupon_code','').strip().upper()
        discount=float(request.form.get('discount',0))
        if not address:
            flash('Please enter a delivery address.','error'); conn.close()
            return redirect(url_for('checkout'))
        total=max(0,subtotal-discount)
        ref='PAY-'+uuid.uuid4().hex[:10].upper()
        conn.execute('INSERT INTO orders (customer_id,total_price,payment_ref,status,address,coupon_code,discount) VALUES (?,?,?,?,?,?,?)',
                     (session['user_id'],total,ref,'paid',address,coupon_code,discount))
        oid=conn.execute('SELECT last_insert_rowid()').fetchone()[0]
        record_timeline(conn,oid,'paid','Order placed and payment received.')
        seller_totals={}
        for i in items:
            conn.execute('INSERT INTO order_items (order_id,product_id,seller_id,quantity,price) VALUES (?,?,?,?,?)',
                         (oid,i['pid'],i['seller_id'],i['quantity'],i['price']))
            conn.execute('UPDATE products SET stock=MAX(0,stock-?) WHERE id=?',(i['quantity'],i['pid']))
            seller_totals[i['seller_id']]=seller_totals.get(i['seller_id'],0)+i['price']*i['quantity']
        for sid,amt in seller_totals.items():
            share=round(amt*(1-discount/subtotal if subtotal else 1)*SELLER_SHARE,2)
            conn.execute('INSERT INTO seller_earnings (seller_id,order_id,amount) VALUES (?,?,?)',(sid,oid,share))
            notify(conn,sid,f'New order #{oid} received! ₹{share:.2f} credited.',url_for('seller_orders'))
        conn.execute('INSERT INTO platform_earnings (order_id,amount) VALUES (?,?)',
                     (oid,round(total*PLATFORM_SHARE,2)))
        if coupon_code:
            conn.execute('UPDATE coupons SET used_count=used_count+1 WHERE code=?',(coupon_code,))
        conn.execute('DELETE FROM cart_items WHERE user_id=?',(session['user_id'],))
        conn.commit(); conn.close()
        flash(f'Order placed! Ref: {ref}','success')
        return redirect(url_for('order_detail',oid=oid))
    conn.close()
    return render_template('customer/checkout.html',items=items,total=subtotal)

