def admin_dashboard():
    conn=get_db()
    stats={
        'users':conn.execute('SELECT COUNT(*) FROM users').fetchone()[0],
        'sellers':conn.execute("SELECT COUNT(*) FROM users WHERE role='seller'").fetchone()[0],
        'products':conn.execute('SELECT COUNT(*) FROM products').fetchone()[0],
        'orders':conn.execute('SELECT COUNT(*) FROM orders').fetchone()[0],
        'revenue':conn.execute('SELECT COALESCE(SUM(total_price),0) FROM orders').fetchone()[0],
        'seller_payouts':conn.execute('SELECT COALESCE(SUM(amount),0) FROM seller_earnings').fetchone()[0],
        'platform_income':conn.execute('SELECT COALESCE(SUM(amount),0) FROM platform_earnings').fetchone()[0]
    }
    recent=conn.execute("""SELECT o.*,u.name as customer_name FROM orders o
                            JOIN users u ON o.customer_id=u.id
                            ORDER BY o.created_at DESC LIMIT 10""",(session['user_id'],)).fetchall()
    # Daily revenue last 7 days
    daily_revenue=[]
    for i in range(6,-1,-1):
        date=(datetime.now()-timedelta(days=i)).strftime('%Y-%m-%d')
        rev=conn.execute("SELECT COALESCE(SUM(total_price),0) FROM orders WHERE DATE(created_at)=?",(date,)).fetchone()[0]
        daily_revenue.append({'date':date,'revenue':rev})
    # Top products
    top_products=conn.execute("""SELECT p.name,SUM(oi.quantity) as sold FROM order_items oi
                                  JOIN products p ON oi.product_id=p.id
                                  GROUP BY p.id ORDER BY sold DESC LIMIT 5""").fetchall()
    # Top sellers
    top_sellers=conn.execute("""SELECT u.name,COALESCE(sp.shop_name,u.name) as shop_name,SUM(se.amount) as earned
                                 FROM seller_earnings se JOIN users u ON se.seller_id=u.id
                                 LEFT JOIN seller_profiles sp ON u.id=sp.user_id
                                 GROUP BY u.id ORDER BY earned DESC LIMIT 5""").fetchall()
    conn.close()
    return render_template('admin/dashboard.html',stats=stats,recent=recent,
                           daily_revenue=daily_revenue,top_products=top_products,top_sellers=top_sellers)

