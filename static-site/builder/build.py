def load_config(path: Path) -> dict:
    with open(path, encoding="utf-8") as f:
        cfg = json.load(f)
    cfg.setdefault("tagline",     "Build the Future Faster Than Ever")
    cfg.setdefault("description", "Cutting-edge solutions for modern teams.")
    cfg.setdefault("cta_text",    "Get Started")
    cfg.setdefault("cta_link",    "contact.html")
    cfg.setdefault("font",        "Inter")
    cfg.setdefault("pages",       {})
    cfg.setdefault("features",    {})
    cfg.setdefault("content",     {})
    cfg.setdefault("plugins",     {})
    f = cfg["features"]
    for k, v in [("dark_mode",True),("animations",True),("blog",True),("portfolio_filter",True),
                 ("pricing_toggle",True),("scroll_progress",True),("back_to_top",True),
                 ("newsletter",True),("counter_animation",True)]:
        f.setdefault(k, v)
    return cfg


def build(cfg: dict, out_dir: Path, mode: str = "static", minify: bool = False) -> list[str]:
    import plugins as plugin_loader
    from exporter import export_flask, export_react

    # Clean output
    if out_dir.exists():
        shutil.rmtree(out_dir)
    out_dir.mkdir(parents=True)

    generated = []

    # Run plugins
    plugin_injections = plugin_loader.run_all(cfg)

    # Generate CSS + JS
    css = generate_css(cfg)
    js  = generate_js(cfg)
    if minify:
        css = minify_css(css)
        js  = minify_js(js)

    # Collect all page HTML
    pages_html: dict[str, str] = {}
    pages_cfg  = cfg.get("pages", {})

    for page_key, page_cfg in pages_cfg.items():
        # Feature gate: blog
        if page_key == "blog" and not cfg["features"].get("blog", True):
            print(f"  ⏭  blog skipped (features.blog=false)")
            continue

        filename = page_cfg.get("file", page_key + ".html")
        print(f"  ✓  {filename}")

        html = assemble_page(page_key, page_cfg, cfg, plugin_injections)
        if minify:
            html = minify_html(html)
        pages_html[filename] = html

    if mode == "flask":
        print("\n  📦 Flask export mode")
        (out_dir / "static" / "css").mkdir(parents=True, exist_ok=True)
        (out_dir / "static" / "js").mkdir(parents=True, exist_ok=True)
        (out_dir / "static" / "css" / "style.css").write_text(css, encoding="utf-8")
        (out_dir / "static" / "js"  / "script.js").write_text(js,  encoding="utf-8")
        generated += ["static/css/style.css", "static/js/script.js"]
        generated += export_flask(cfg, out_dir, pages_html)

    elif mode == "react":
        print("\n  ⚛  React export mode")
        (out_dir / "style.css").write_text(css, encoding="utf-8")
        (out_dir / "script.js").write_text(js,  encoding="utf-8")
        generated += ["style.css", "script.js"]
        generated += export_react(cfg, out_dir, pages_html)

    else:  # static (default)
        (out_dir / "style.css").write_text(css, encoding="utf-8")
        (out_dir / "script.js").write_text(js,  encoding="utf-8")
        generated += ["style.css", "script.js"]
        for filename, html in pages_html.items():
            (out_dir / filename).write_text(html, encoding="utf-8")
            generated.append(filename)

    print(f"\n✅ Built {len(generated)} files → {out_dir}  [mode={mode}{'  minified' if minify else ''}]")
    return generated


