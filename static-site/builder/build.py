def main():
    parser = argparse.ArgumentParser(
        description="Website Builder System v2 — template engine + plugins + export modes",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""Examples:
  python build.py
  python build.py --config my.json --out ./dist
  python build.py --mode flask
  python build.py --mode react
  python build.py --minify
  python build.py --watch
  python build.py --preview --port 3000
  python build.py --mode flask --minify --preview
"""
    )
    parser.add_argument("--config",  default=str(DEFAULT_CONFIG), help="Config JSON path")
    parser.add_argument("--out",     default=str(DEFAULT_OUT),    help="Output directory")
    parser.add_argument("--mode",    default="static", choices=["static","flask","react"], help="Export mode")
    parser.add_argument("--minify",  action="store_true", help="Minify HTML/CSS/JS output")
    parser.add_argument("--watch",   action="store_true", help="Watch for changes and auto-rebuild")
    parser.add_argument("--preview", action="store_true", help="Start preview server after build")
    parser.add_argument("--port",    type=int, default=8080, help="Preview server port")
    args = parser.parse_args()

    cfg_path = Path(args.config)
    out_dir  = Path(args.out)

    if not cfg_path.exists():
        print(f"❌ Config not found: {cfg_path}")
        return

    print(f"📖 Config:  {cfg_path}")
    cfg = load_config(cfg_path)
    print(f"🏗  Site:    {cfg['site_name']}")
    print(f"   Mode:    {args.mode}")
    print(f"   Output:  {out_dir}")
    print(f"   Minify:  {args.minify}")
    print(f"   Pages:   {', '.join(cfg['pages'].keys())}")
    print(f"   Plugins: {', '.join(k for k,v in cfg.get('plugins',{}).items() if v.get('enabled'))}\n")

    build(cfg, out_dir, args.mode, args.minify)

    if args.watch and args.preview:
        # Preview in background thread, watch in foreground
        t = threading.Thread(target=serve, args=(out_dir, args.port), daemon=True)
        t.start()
        watch(cfg_path, out_dir, args.mode, args.minify)
    elif args.watch:
        watch(cfg_path, out_dir, args.mode, args.minify)
    elif args.preview:
        serve(out_dir, args.port)


if __name__ == "__main__":
    main()
