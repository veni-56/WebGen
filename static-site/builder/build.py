def assemble_page(page_key: str, page_cfg: dict, cfg: dict, plugin_injections: dict) -> str:
    """
    Assemble a complete HTML page from its sections using the template engine.
    """
    ctx = build_context(cfg, page_key)
    ctx = _enrich_context(ctx, cfg)

    # Plugin slots
    ctx["plugin_head"]    = plugin_injections.get("head",    "")
    ctx["plugin_body"]    = plugin_injections.get("body",    "")
    ctx["plugin_scripts"] = plugin_injections.get("scripts", "")
    ctx["page_description"] = cfg.get("description", "")

    # Assemble body from sections
    sections = page_cfg.get("sections", [])
    body_parts = []
    for section_name in sections:
        try:
            section_html = render_file(section_name, ctx)
            body_parts.append(section_html)
        except FileNotFoundError:
            print(f"    ⚠  Section template '{section_name}.html' not found — skipped")

    ctx["page_body"] = "\n".join(body_parts)

    # Render full page through _base.html
    return render_file("_base", ctx)


