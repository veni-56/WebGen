def watch(cfg_path: Path, out_dir: Path, mode: str, minify: bool) -> None:
    """Watch config + templates for changes and auto-rebuild."""
    watch_dirs  = [HERE / "templates", HERE / "plugins"]
    watch_files = [cfg_path]

    def get_mtimes():
        mtimes = {}
        for f in watch_files:
            if f.exists():
                mtimes[str(f)] = f.stat().st_mtime
        for d in watch_dirs:
            if d.exists():
                for f in d.rglob("*"):
                    if f.is_file():
                        mtimes[str(f)] = f.stat().st_mtime
        return mtimes

    print(f"👁  Watching for changes... (Ctrl+C to stop)\n")
    last = get_mtimes()

    while True:
        time.sleep(1)
        current = get_mtimes()
        changed = [k for k in current if current[k] != last.get(k)]
        if changed:
            print(f"\n🔄 Change detected: {', '.join(Path(c).name for c in changed)}")
            try:
                cfg = load_config(cfg_path)
                build(cfg, out_dir, mode, minify)
            except Exception as e:
                print(f"  ❌ Build error: {e}")
            last = get_mtimes()


