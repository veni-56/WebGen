"""
exporter.py — Export modes for Website Builder System

Modes:
  static  — plain HTML/CSS/JS (default)
  flask   — wraps output in a Flask app.py
  react   — exports basic React components
"""

from pathlib import Path


# ═══════════════════════════════════════════════════════════════════════════════
# FLASK EXPORT
# ═══════════════════════════════════════════════════════════════════════════════

def export_flask(cfg: dict, out_dir: Path, pages: dict[str, str]) -> list[str]:
    """
    Wrap the static site in a minimal Flask app.
    - Moves HTML files to templates/
    - Generates app.py with routes for each page
    - Generates requirements.txt
    """
    name = cfg["site_name"]
    (out_dir / "templates").mkdir(exist_ok=True)
    (out_dir / "static" / "css").mkdir(parents=True, exist_ok=True)
    (out_dir / "static" / "js").mkdir(parents=True, exist_ok=True)

    generated = []

    # Move HTML → templates/
    for filename, html in pages.items():
        # Rewrite static asset paths for Flask
        html = html.replace('href="style.css"',   "href=\"{{ url_for('static', filename='css/style.css') }}\"")
        html = html.replace('src="script.js"',    "src=\"{{ url_for('static', filename='js/script.js') }}\"")
        (out_dir / "templates" / filename).write_text(html, encoding="utf-8")
        generated.append(f"templates/{filename}")

    # Build route map
    route_map = {
        "index.html":        ("/",              "home"),
        "about.html":        ("/about",         "about"),
        "services.html":     ("/services",      "services"),
        "portfolio.html":    ("/portfolio",     "portfolio"),
        "blog.html":         ("/blog",          "blog"),
        "contact.html":      ("/contact",       "contact"),
        "pricing.html":      ("/pricing",       "pricing"),
        "team.html":         ("/team",          "team"),
        "faq.html":          ("/faq",           "faq"),
        "testimonials.html": ("/testimonials",  "testimonials"),
    }

    routes = ""
    for filename in pages:
        if filename in route_map:
            path, fn = route_map[filename]
            routes += f"""
@app.route('{path}')
def {fn}():
    return render_template('{filename}')
"""

    app_py = f'''"""
app.py — {name}
Generated by Website Builder System (Flask export mode)
"""
import os
from flask import Flask, render_template, request, redirect, url_for, flash

app = Flask(__name__)
app.secret_key = os.environ.get('SECRET_KEY', 'change-this-in-production')

{routes}

@app.route('/contact', methods=['POST'])
def contact_submit():
    name    = request.form.get('firstName', '') + ' ' + request.form.get('lastName', '')
    email   = request.form.get('email', '')
    message = request.form.get('message', '')
    # TODO: send email or save to DB
    flash(f'Thanks {{name}}! We will get back to you soon.', 'success')
    return redirect(url_for('contact'))

if __name__ == '__main__':
    app.run(debug=True)
'''

    (out_dir / "app.py").write_text(app_py, encoding="utf-8")
    (out_dir / "requirements.txt").write_text("Flask==3.0.0\nWerkzeug==3.0.1\n", encoding="utf-8")
    generated += ["app.py", "requirements.txt"]

    readme = f"""{name} — Flask Export
{'=' * 40}

QUICK START
-----------
1. pip install -r requirements.txt
2. python app.py
3. Open http://localhost:5000

Generated by Website Builder System
"""
    (out_dir / "README.txt").write_text(readme, encoding="utf-8")
    generated.append("README.txt")

    return generated


